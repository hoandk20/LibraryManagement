    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (bookId == -1) {
            JOptionPane.showMessageDialog(null, "Bạn phải kiểm tra sách trước!");
            return;
        }
        int quantity = (int) jspinQuantity.getValue();
        if (quantity <= 0) {
            JOptionPane.showMessageDialog(null, "Só lượng phải lớn hơn 0!");
            return;
        }
        if (quantity > remain) {
            JOptionPane.showMessageDialog(null, "Không đủ sách trong kho!");
            return;
        }
        String studentName = txtStudentName.getText();
        String studentClass = txtClass.getText();
        String studentId = txtStudentId.getText();
        if (!(check.checkvalidString(studentName) && check.checkvalidString(studentClass) && check.checkvalidString(studentId))) {
            JOptionPane.showMessageDialog(null, "Không được bỏ trống");
            return;
        }
        LocalDate today = LocalDate.now();
        Date d = Date.valueOf(today);
        
        borrow b = new borrow();
        b.setBookId(bookId);
        b.setQuantity(quantity);
        b.setStudentName(studentName);
        b.setStudentClass(studentClass);
        b.setStudentID(studentId);
        b.setTimeGet(d);
        b.setStatus(false);
        try {
            borrowDAO bDAO = new borrowDAO();
            bDAO.insert(b);
            JOptionPane.showMessageDialog(null, "Thành công!");
            showbook();
            jspinQuantity.setValue(0);
            return;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Thất bại");
            return;
        }
        

    }//GEN-LAST:event_btnAddActionPerformed


