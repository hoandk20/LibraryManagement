    void showbook() {
        String TempBookId = txtBookId.getText();
        if (!check.checkvalidString(TempBookId)) {
            JOptionPane.showMessageDialog(null, "Không được bỏ trống!");
            return;
        }
        if (!check.checkValidInt(TempBookId)) {
            JOptionPane.showMessageDialog(null, "ID Không hợp lệ!");
            return;
            
        }
        bookDAO bDAO = new bookDAO();
        bookId = Integer.parseInt(TempBookId);
        book b = bDAO.getAllbookByID(bookId);
        if (b == null) {
            JOptionPane.showMessageDialog(null, "Sách không tồn tại!");
            return;
        }
        txtbookName.setText("" + b.getName());
        txtAuthorName.setText("" + b.getAuthor().getAuthorName());
        borrowDAO brDAO = new borrowDAO();
        
        int borrowquantity = brDAO.GetTotalBorrowbook(bookId);
        remain = b.getQuantity() - borrowquantity;
        available = b.getQuantity();
        txtRemain.setText(remain + "/" + available);
        txtBookId.enable(false);
    }
